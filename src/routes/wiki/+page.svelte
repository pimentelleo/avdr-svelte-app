		*Conduta de Clans: Qualquer clan que desrespeite algumas das regras aqui citadas, pode e será
		deletado e os membros infratores punidos. Sendo que o clan só será deletado se a maioria dos
		membros do clan participarem da infração. PUNIÇÃO: BAN
	</p>
	<p>*Qualquer reincidência(x3) na quebra de regras resultará em ban.</p>
