		*Abuso de Bugs: Um bug geralmente é um problema de ordem técnica no servidor. A utilização de
		BUGs pode fornecer vantagem para um determinado jogador e qualquer circunstância que possa dar
		vantagem para somente alguns jogadores não é permitido. Portanto, é um ato considerado como
